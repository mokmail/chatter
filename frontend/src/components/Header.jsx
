import React, { useState, useRef, useEffect } from 'react'
import { useTheme } from '../hooks/useTheme'

const Header = ({ onClearHistory, title, models = [], currentModel, currentProviderId, onModelSelect, isRoleplayActive, roleplayCharacter, onRoleplayClick }) => {
  const { theme, toggleTheme } = useTheme()
  const [showModels, setShowModels] = useState(false)
  const dropdownRef = useRef(null)

  useEffect(() => {
    const handleClickOutside = (e) => {
      if (dropdownRef.current && !dropdownRef.current.contains(e.target)) {
        setShowModels(false)
      }
    }
    document.addEventListener('mousedown', handleClickOutside)
    return () => document.removeEventListener('mousedown', handleClickOutside)
  }, [])

  // Group models by provider
  const groupedModels = models.reduce((acc, model) => {
    if (!acc[model.provider_name]) acc[model.provider_name] = []
    acc[model.provider_name].push(model)
    return acc
  }, {})

  return (
    <header className="relative flex items-center justify-between px-5 py-3 border-b overflow-visible z-30"
      style={{ borderColor: 'var(--border)', backgroundColor: 'var(--bg-secondary)' }}>
      <div className="flex items-center gap-3">
        <div className="relative" ref={dropdownRef}>
          <button 
            onClick={() => setShowModels(!showModels)}
            className="flex items-center gap-2 px-3 py-1.5 rounded-lg transition-all group"
            style={{
              background: 'var(--surface)',
              border: '1px solid var(--border)',
            }}
          >
            <div className="w-2 h-2 rounded-full"
              style={{ backgroundColor: 'var(--success)' }} />
            <span className="text-sm font-medium">
              {title}
            </span>
            <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20" fill="currentColor" 
              className={`w-4 h-4 transition-transform duration-200 ${showModels ? 'rotate-180' : ''}`}
              style={{ color: 'var(--text-tertiary)' }}>
              <path fillRule="evenodd" d="M5.22 8.22a.75.75 0 0 1 1.06 0L10 11.94l3.72-3.72a.75.75 0 1 1 1.06 1.06l-4.25 4.25a.75.75 0 0 1-1.06 0L5.22 9.28a.75.75 0 0 1 0-1.06Z" clipRule="evenodd" />
            </svg>
          </button>

          {showModels && (
            <div className="absolute top-full left-0 mt-2 w-72 rounded-xl overflow-hidden animate-fade-in-scale"
              style={{ background: 'var(--bg-secondary)', border: '1px solid var(--border)', boxShadow: 'var(--dropdown-shadow)' }}>
              <div className="p-2 max-h-[60vh] overflow-y-auto">
                {Object.keys(groupedModels).length === 0 ? (
                  <div className="px-4 py-8 text-center text-xs" style={{ color: 'var(--text-tertiary)' }}>
                    No models found. Check your connections.
                  </div>
                ) : (
                  Object.entries(groupedModels).map(([providerName, providerModels]) => (
                    <div key={providerName} className="mb-2 last:mb-0">
                      <div className="px-3 py-1.5 text-[10px] font-semibold uppercase tracking-widest mb-1"
                        style={{ color: 'var(--text-tertiary)' }}>
                        {providerName}
                      </div>
                      <div className="space-y-0.5">
                        {providerModels.map((m) => (
                          <button
                            key={`${m.provider_id}-${m.id}`}
                            onClick={() => {
                              onModelSelect(m.id, m.provider_id)
                              setShowModels(false)
                            }}
                            className={`w-full flex items-center justify-between px-3 py-2 rounded-lg text-sm transition-all ${
                              currentModel === m.id && currentProviderId === m.provider_id
                                ? 'font-semibold'
                                : ''
                            }`}
                            style={{
                              background: currentModel === m.id && currentProviderId === m.provider_id ? 'var(--surface)' : 'transparent',
                              color: currentModel === m.id && currentProviderId === m.provider_id ? 'var(--text)' : 'var(--text-secondary)',
                            }}
                            onMouseEnter={e => {
                              if (currentModel !== m.id || currentProviderId !== m.provider_id) {
                                e.currentTarget.style.background = 'var(--surface)'
                                e.currentTarget.style.color = 'var(--text)'
                              }
                            }}
                            onMouseLeave={e => {
                              if (currentModel !== m.id || currentProviderId !== m.provider_id) {
                                e.currentTarget.style.background = 'transparent'
                                e.currentTarget.style.color = 'var(--text-secondary)'
                              }
                            }}
                          >
                            <span className="truncate">{m.name}</span>
                            {currentModel === m.id && currentProviderId === m.provider_id && (
                              <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20" fill="currentColor" className="w-4 h-4"
                                style={{ color: 'var(--text)' }}>
                                <path fillRule="evenodd" d="M16.704 4.153a.75.75 0 0 1 .143 1.052l-8 10.5a.75.75 0 0 1-1.127.075l-4.5-4.5a.75.75 0 0 1 1.06-1.06l3.894 3.893 7.48-9.817a.75.75 0 0 1 1.05-.143Z" clipRule="evenodd" />
                              </svg>
                            )}
                          </button>
                        ))}
                      </div>
                    </div>
                  ))
                )}
              </div>
            </div>
          )}
        </div>
      </div>

      <div className="flex items-center gap-1">
        {isRoleplayActive && roleplayCharacter && (
          <button
            onClick={onRoleplayClick}
            className="flex items-center gap-2 px-3 py-1.5 rounded-lg transition-all"
            style={{
              background: 'var(--surface)',
              border: '1px solid var(--border)',
              color: 'var(--text)',
            }}
            title="Roleplay mode active - click to manage"
          >
            <span className="text-sm font-medium">{roleplayCharacter.name}</span>
            <div className="w-2 h-2 rounded-full" style={{ background: 'var(--success)' }} />
          </button>
        )}
        
        <button
          onClick={onRoleplayClick}
          className="p-2 rounded-lg transition-all duration-200"
          title="Roleplay Engine"
          style={{ color: 'var(--text-tertiary)' }}
          onMouseEnter={e => {
            e.currentTarget.style.backgroundColor = 'var(--surface)'
            e.currentTarget.style.color = 'var(--text)'
          }}
          onMouseLeave={e => {
            e.currentTarget.style.backgroundColor = 'transparent'
            e.currentTarget.style.color = 'var(--text-tertiary)'
          }}
        >
          <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className="w-[18px] h-[18px]">
            <path fillRule="evenodd" d="M9 4.5a.75.75 0 01.721.544l.813 2.846a3.75 3.75 0 002.576 2.576l2.846.813a.75.75 0 010 1.442l-2.846.813a3.75 3.75 0 00-2.576 2.576l-.813 2.846a.75.75 0 01-1.442 0l-.813-2.846a3.75 3.75 0 00-2.576-2.576l-2.846-.813a.75.75 0 010-1.442l2.846-.813A3.75 3.75 0 007.466 7.89l.813-2.846A.75.75 0 019 4.5zM18 1.5a.75.75 0 01.728.568l.258 1.036c.236.94.97 1.674 1.91 1.91l1.036.258a.75.75 0 010 1.456l-1.036.258c-.94.236-1.674.97-1.91 1.91l-.258 1.036a.75.75 0 01-1.456 0l-.258-1.036a2.625 2.625 0 00-1.91-1.91l-1.036-.258a.75.75 0 010-1.456l1.036-.258a2.625 2.625 0 001.91-1.91l.258-1.036A.75.75 0 0118 1.5zM16.5 15a.75.75 0 01.712.513l.394 1.183c.15.443.474.767.917.917l1.183.395a.75.75 0 010 1.422l-1.183.395c-.443.15-.767.474-.917.917l-.395 1.183a.75.75 0 01-1.422 0l-.395-1.183a1.125 1.125 0 00-.917-.917l-1.183-.395a.75.75 0 010-1.422l1.183-.395a1.125 1.125 0 00.917-.917l.395-1.183A.75.75 0 0116.5 15z" clipRule="evenodd" />
          </svg>
        </button>
        <button
          onClick={toggleTheme}
          className="p-2 rounded-lg transition-all duration-200"
          title={theme === 'dark' ? 'Switch to light mode' : 'Switch to dark mode'}
          style={{ color: 'var(--text-tertiary)' }}
          onMouseEnter={e => {
            e.currentTarget.style.backgroundColor = 'var(--surface)'
            e.currentTarget.style.color = 'var(--text)'
          }}
          onMouseLeave={e => {
            e.currentTarget.style.backgroundColor = 'transparent'
            e.currentTarget.style.color = 'var(--text-tertiary)'
          }}
        >
          {theme === 'dark' ? (
            <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-[18px] h-[18px]">
              <path strokeLinecap="round" strokeLinejoin="round" d="M12 3v2.25m6.364.386l-1.591 1.591M21 12h-2.25m-.386 6.364l-1.591-1.591M12 18.75V21m-4.773-4.227l-1.591 1.591M5.25 12H3m4.227-4.773L5.636 5.636M15.75 12a3.75 3.75 0 11-7.5 0 3.75 3.75 0 017.5 0z" />
            </svg>
          ) : (
            <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-[18px] h-[18px]">
              <path strokeLinecap="round" strokeLinejoin="round" d="M21.752 15.002A9.718 9.718 0 0118 15.75c-5.385 0-9.75-4.365-9.75-9.75 0-1.33.266-2.597.748-3.752A9.753 9.753 0 003 11.25C3 16.635 7.365 21 12.75 21a9.753 9.753 0 009.002-5.998z" />
            </svg>
          )}
        </button>
        <button
          onClick={onClearHistory}
          className="p-2 rounded-lg transition-all duration-200"
          title="Clear chat history"
          style={{ color: 'var(--text-tertiary)' }}
          onMouseEnter={e => {
            e.currentTarget.style.backgroundColor = 'var(--surface)'
            e.currentTarget.style.color = 'var(--danger-icon)'
          }}
          onMouseLeave={e => {
            e.currentTarget.style.backgroundColor = 'transparent'
            e.currentTarget.style.color = 'var(--text-tertiary)'
          }}
        >
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-[18px] h-[18px]">
            <path strokeLinecap="round" strokeLinejoin="round" d="M16.023 9.348h4.992v-.001M2.985 19.644v-4.992m0 0h4.992m-4.993 0 3.181 3.183a8.25 8.25 0 0013.803-3.7M4.031 9.865a8.25 8.25 0 0 1 13.803-3.7l3.181 3.182m0-4.991v4.99" />
          </svg>
        </button>
      </div>
    </header>
  )
}

export default Header
